package bean.chat;

public class Custom {

    private String from;
    private long dtMs;
    private String msg;
    private String picUrl;
    
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public long getDtMs() {
		return dtMs;
	}
	public void setDtMs(long dtMs) {
		this.dtMs = dtMs;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getPicUrl() {
		return picUrl;
	}
	public void setPicUrl(String picUrl) {
		this.picUrl = picUrl;
	}
    
    


}
